using UnityEngine;
using System.Collections;

public class RideFlyingCircus : MonoBehaviour {
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
//
//										Andre "AEG" Buerger - VIS-Games 2012
//
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------

    GameObject arm1;
    GameObject arm2;
    GameObject gondel;

    float arm1_rot;
    float arm2_rot;
    float gondel_rot;

//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
void Awake()
{
    arm1 = transform.Find("plattform/staender/arm1").gameObject;
    arm2 = arm1.transform.Find("arm2").gameObject;
    gondel = arm2.transform.Find("gondel").gameObject;

    arm1_rot = 0.0f;
    arm2_rot = 0.0f;
    gondel_rot = 0.0f;
}
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
void Start()
{

}
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
void Update()
{
    //-- arm1 rotation
    arm1_rot = (arm1_rot + Time.deltaTime * 40.0f) % 360.0f;
    arm1.transform.localEulerAngles = new Vector3(0.0f, 0.0f, arm1_rot);

    //-- arm2 rotation
    arm2_rot = (arm2_rot - Time.deltaTime * 80.0f) % 360.0f;
    arm2.transform.localEulerAngles = new Vector3(0.0f, 0.0f, arm2_rot);

    //-- gondel rotation
    gondel_rot = (gondel_rot + Time.deltaTime * 110.0f) % 360.0f;
    gondel.transform.localEulerAngles = new Vector3(0.0f, gondel_rot, 0.0f);

}
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
}
