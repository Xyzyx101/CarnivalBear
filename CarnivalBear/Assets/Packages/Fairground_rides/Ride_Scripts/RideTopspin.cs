using UnityEngine;
using System.Collections;

public class RideTopspin : MonoBehaviour {
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
//
//										Andre "AEG" Buerger - VIS-Games 2012
//
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------

    GameObject arme;
    GameObject gondel;

    float arm_rot;
    float gondel_sin;
    int   arm_mode;
    float arm_timer;
    bool  arm_direction;
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
void Awake()
{
    arme = transform.Find("rotationsarm_achse").gameObject;
    gondel = arme.transform.Find("gondel").gameObject;

    arm_direction = true;
    arm_mode = 0;
    arm_timer = 0.0f;
    arm_rot = 0.0f;
    gondel_sin = 0.0f;
}
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
void Start()
{
    Update();
}
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
void Update()
{

    //-- arm rotation
    if(arm_mode == 0)
    {
        arm_timer -= Time.deltaTime * 1.0f;
        if(arm_timer < 0.0f)
        {
            arm_timer = Random.Range(16.0f, 32.0f);
            arm_mode = 1;
            arm_direction = !arm_direction;
        }
    }
    else
    {
        arm_timer -= Time.deltaTime * 1.0f;
        if(arm_timer < 0.0f)
        {
            arm_timer = Random.Range(2.0f, 5.0f);
            arm_mode = 0;
        }
        else
        {
            if(arm_direction == true)
                arm_rot += Time.deltaTime * 60.0f;
            else
                arm_rot -= Time.deltaTime * 60.0f;
        }
    }
    arme.transform.localEulerAngles = new Vector3(arm_rot, 0.0f, 0.0f);


    //-- gondel rotation
    float sin1 = (Mathf.Sin(gondel_sin)) * 45.0f;
 
    gondel.transform.localEulerAngles = new Vector3(-arm_rot + sin1, 0.0f, 0.0f);

    gondel_sin += ((5.0f * Time.deltaTime) % 360.0f);


}
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
}
