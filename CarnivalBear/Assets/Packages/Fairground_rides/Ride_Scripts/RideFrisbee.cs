using UnityEngine;
using System.Collections;

public class RideFrisbee : MonoBehaviour {
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
//
//										Andre "AEG" Buerger - VIS-Games 2012
//
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------

    GameObject arm;
    GameObject gondel;

    float arm_sin;
    float arm_rot;
    float gondel_rot;

//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
void Awake()
{
    arm = transform.Find("unterbau/plattform/staender/arm").gameObject;
    gondel = arm.transform.Find("gondel").gameObject;

    arm_sin = 0.0f;
    gondel_rot = 0.0f;
}
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
void Start()
{
    Update();
}
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
void Update()
{
    //-- arm rotation
    arm_rot = Mathf.Sin(arm_sin) * 90.0f;
    arm_sin = (arm_sin + Time.deltaTime * 1.0f) % 360.0f;

    arm.transform.localEulerAngles = new Vector3(0.0f, 0.0f, arm_rot);

    //-- gondel rotation
    gondel_rot = (gondel_rot + Time.deltaTime * 80.0f) % 360.0f;

    gondel.transform.localEulerAngles = new Vector3(0.0f, gondel_rot, 0.0f);
}
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
}
